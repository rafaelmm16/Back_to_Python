def dias_da_semana(x):
  if x==1:
    print ("Hoje é Domingo")
  if x==2:
    print ("Hoje é Segunda")
  if x==3:
    print ("Hoje é Terça")
  if x==4:
    print ("Hoje é Quarta")
  if x==5:
    print ("Hoje é Quinta")
  if x==6:
    print ("Hoje é Sexta")
  if x==7:
    print ("Hoje é Sabado")
  if x>=8:
    print ("Valor Invalido")
  
